            <script>
            document.addEventListener('DOMContentLoaded', function() {
                const readMoreBtn = document.getElementById('read-more-btn');
                const moreHistory = document.getElementById('more-history');
                const btnText = document.getElementById('btn-text');
                const btnIcon = document.getElementById('btn-icon');

                if (readMoreBtn && moreHistory) {
                    readMoreBtn.addEventListener('click', function(e) {
                        e.preventDefault();
                        
                        // Check if it's currently hidden (or in the process of hiding)
                        const isHidden = moreHistory.classList.contains('opacity-0') || moreHistory.classList.contains('hidden');

                        if (isHidden) {
                            // Show content
                            moreHistory.classList.remove('hidden');
                            // Small delay to allow display:block to apply before opacity transition
                            setTimeout(() => {
                                moreHistory.classList.remove('opacity-0');
                            }, 10);
                            
                            btnText.textContent = "Read Less";
                            btnIcon.style.transform = "rotate(180deg)";
                            // Reset hover effect override
                            btnIcon.classList.remove('group-hover:translate-x-1');
                        } else {
                            // Hide content
                            moreHistory.classList.add('opacity-0');
                            
                            // Wait for transition to finish before hiding
                            setTimeout(() => {
                                moreHistory.classList.add('hidden');
                            }, 500);
                            
                            btnText.textContent = "Read More History";
                            btnIcon.style.transform = "rotate(0deg)";
                            // Restore hover effect
                            btnIcon.classList.add('group-hover:translate-x-1');
                        }
                    });
                }
            });
            </script>
